class FileProcessor:
    """
    A collection of processing layer functions that work with Json files

    ChangeLog: (Who, When, What)
    RRoot,1.1.2030,Created Class
    """
    @staticmethod
    def read_data_from_file(file_name: str, student_data: list):
        """ This function reads data from a json file and loads it into a list of dictionary rows
        then returns the list filled with student data.

        ChangeLog: (Who, When, What)
        RRoot,1.1.2030,Created function

        :param file_name: string data with name of file to read from
        :param student_data: list to be filled with student data (not used in this implementation)

        :return: list
        """
        student_objects = []
        try:
            # Get a list of dictionary rows from the data file
            file = open(file_name, "r")
            json_students = json.load(file)
            # Convert the list of dictionary rows into a list of Student objects
            student_objects = []
            # TODO replace this line of code to convert dictionary data to Student data
            student_objects = [Student(first_name=student["FirstName"],
                                       last_name=student["LastName"],
                                       course_name=student["CourseName"])
                               for student in json_students]

            file.close()

        except Exception as e:
            IO.output_error_messages(message="Error: There was a problem with reading the file.", error=e)

        finally:
            if file.closed == False:
                file.close()

        return student_objects

    @staticmethod
    def write_data_to_file(file_name: str, student_data: list):
        """ This function writes data to a json file with data from a list of dictionary rows

        ChangeLog: (Who, When, What)
        RRoot,1.1.2030,Created function

        :param file_name: string data with name of file to write to
        :param student_data: list of dictionary rows to be writen to the file

        :return: None
        """

        try:
            file = open(file_name, "w")
            dict_list = []
            for student in student_data:
                if isinstance(student, dict):
                    dict_list.append(student)
                else:
                    dict_list.append({
                        "FirstName": student.first_name,
                        "LastName": student.last_name,
                        "CourseName": student.course_name
                    })
            json.dump(dict_list, file, indent=4)
            file.close()
            IO.output_student_courses(student_data=student_data)
        except Exception as e:
            message = "Error: There was a problem with writing to the file.\n"
            message += "Please check that the file is not open by another program."
            IO.output_error_messages(message=message,error=e)
        finally:
            if file.closed == False:
                file.close()


